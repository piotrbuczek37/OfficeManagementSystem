    private void bZapiszActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bZapiszActionPerformed
        // TODO add your handling code here:
//        dane.get(i).getNrWydatku() != Integer.parseInt(tNrWydatku.getText())
        if(tNrWydatku.getText().equals("Generowany automatycznie")){
            bd.zapis(tPrzedmiot.getText(),
        tDataKupna.getText(),
        tKoszt.getText(),
        tNrPracownika.getText());
        } else if(dane.get(i).getNrWydatku() == Integer.parseInt(tNrWydatku.getText())){
            bd.modyfikuj(tNrWydatku.getText(),
                    tPrzedmiot.getText(),
        tDataKupna.getText(),
        tKoszt.getText(),
        tNrPracownika.getText());
        }
        
        
        this.zablokujTekst();
        this.bNowy.setEnabled(true);
        this.bZapisz.setEnabled(false);
        this.bEdytuj.setEnabled(true);
        this.bUsun.setEnabled(true);
        this.bWyswietl.setEnabled(true);
        wydatkiComboBox.setEnabled(true);
        dane = bd.odczyt();
        wydatkiComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(initializeComboBox(dane)));
        wydatkiComboBox.setSelectedItem(String.valueOf(dane.get(dane.size()-1).getNrWydatku()));
    }//GEN-LAST:event_bZapiszActionPerformed

