    private void wyswietl(int i){
        Wydatek w = dane.get(i);
        tNrWydatku.setText(""+w.getNrWydatku());
        tPrzedmiot.setText(w.getPrzedmiot());
        tDataKupna.setText(w.getDataKupna());
        tKoszt.setText(""+w.getKoszt());
        tNrPracownika.setText(""+w.getNrPracownika());
        tBudzet.setText(String.valueOf(obliczBudzet(totalBudzet)));
    }
    
    private void wyswietlWydatek(int nrWydatku){
        for(int i=0;i<dane.size();i++){
            if(((Wydatek)dane.get(i)).getNrWydatku()==nrWydatku){
                this.i = i;
                wyswietl(i);
            }
        }
    }
    
    private String[] initializeComboBox(ArrayList<Wydatek> wydatki){
        String[] numbers = new String[wydatki.size()];
        int i = 0;
        for(Wydatek wydatek : wydatki){
            numbers[i] = String.valueOf(wydatek.getNrWydatku());
            i++;
        }
        return numbers;
    }
    
    private double obliczBudzet(double totalBudzet){
        double aktualnyBudzet = totalBudzet;
        for(int i=0;i<dane.size();i++){
            aktualnyBudzet -= dane.get(i).getKoszt();
        }
        return aktualnyBudzet;
    }
    
