    private void bZapiszActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bZapiszActionPerformed
        // TODO add your handling code here:
        if(tNumerKarty.isEnabled()){
            bd.zapis(tNumerKarty.getText(),
                    RodzajKarty.valueOf(rodzajeComboBox.getSelectedItem().toString()),
           tDataWykupienia.getText(),
        tDataZakonczenia.getText(),
        tNrPracownika.getText());
        } else if(!tNumerKarty.isEnabled()){
            bd.modyfikuj(tNumerKarty.getText(),
                    RodzajKarty.valueOf(rodzajeComboBox.getSelectedItem().toString()),
           tDataWykupienia.getText(),
        tDataZakonczenia.getText(),
        tNrPracownika.getText());
        }
        this.zablokujTekst();
        this.bNowy.setEnabled(true);
        this.bZapisz.setEnabled(false);
        this.bEdytuj.setEnabled(true);
        this.bUsun.setEnabled(true);
        this.bWyswietl.setEnabled(true);
        kartyComboBox.setEnabled(true);
        dane = bd.odczyt();
        kartyComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(initializeComboBox(dane)));
        kartyComboBox.setSelectedItem(tNumerKarty.getText());
    }//GEN-LAST:event_bZapiszActionPerformed

