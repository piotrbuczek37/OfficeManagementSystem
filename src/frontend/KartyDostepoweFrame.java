    private void wyswietl(int i){
        KartaDostepowa k = dane.get(i);
        tNumerKarty.setText(""+k.getNrKarty());
        rodzajeComboBox.setSelectedItem(k.getRodzajKarty().toString());
        tDataWykupienia.setText(k.getDataWykupienia());
        tDataZakonczenia.setText(k.getDataZakonczenia());
        tNrPracownika.setText(""+k.getNrPracownika());
    }
    
    private void wyswietlKarte(int nrKarty){
        for(int i=0;i<dane.size();i++){
            if(((KartaDostepowa)dane.get(i)).getNrKarty()==nrKarty){
                this.i = i;
                wyswietl(i);
            }
        }
    }

    private String[] initializeComboBox(ArrayList<KartaDostepowa> karty){
        String[] numbers = new String[karty.size()];
        int i = 0;
        for(KartaDostepowa karta : karty){
            numbers[i] = String.valueOf(karta.getNrKarty());
            i++;
        }
        return numbers;
    }
    
