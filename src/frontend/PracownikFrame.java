    private void wyswietl(int i){
        Pracownik p = dane.get(i);
        tNrPracownika.setText(""+p.getNrPracownika());
        tImie.setText(p.getImie());
        tNazwisko.setText(p.getNazwisko());
        tNumerTelefonu.setText(""+p.getNumerTelefonu());
        tMiejscowosc.setText(p.getMiejscowosc());
        tKodpocztowy.setText(p.getKodPocztowy());
        tUlica.setText(p.getUlica());
        tStanowisko.setText(p.getStanowisko());
        tDataRozpoczecia.setText(p.getDataRozpoczeniaPracy());        
        tDataZakonczenia.setText(p.getDataZakonczeniaPracy());

    }
    
    public void wyswietlPracownika(long nrPracownika){
        for(int i=0;i<dane.size();i++){
            if(((Pracownik)dane.get(i)).getNrPracownika()==nrPracownika){
                this.i = i;
                wyswietl(i);
                pracownicyComboBox.setSelectedItem(String.valueOf(nrPracownika));
            }
        }
    }
    
    private String[] initializeComboBox(ArrayList<Pracownik> pracownicy){
        String[] numbers = new String[pracownicy.size()];
        int i = 0;
        for(Pracownik pracownik : pracownicy){
            numbers[i] = String.valueOf(pracownik.getNrPracownika());
            i++;
        }
        return numbers;
    }
    
