    private void bZapiszActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bZapiszActionPerformed
        // TODO add your handling code here:
        if(tNrPracownika.isEnabled()){
            bd.zapis(tNrPracownika.getText(),
        tImie.getText(),
        tNazwisko.getText(),
        tNumerTelefonu.getText(),
        tMiejscowosc.getText(),
        tKodpocztowy.getText(),
        tUlica.getText(),
        tStanowisko.getText(),
        tDataRozpoczecia.getText(),        
        tDataZakonczenia.getText());
        } else if(!tNrPracownika.isEnabled()){
            bd.modyfikuj(tNrPracownika.getText(),
        tImie.getText(),
        tNazwisko.getText(),
        tNumerTelefonu.getText(),
        tMiejscowosc.getText(),
        tKodpocztowy.getText(),
        tUlica.getText(),
        tStanowisko.getText(),
        tDataRozpoczecia.getText(),        
        tDataZakonczenia.getText());
        }
        
        this.zablokujTekst();
        this.bNowy.setEnabled(true);
        this.bZapisz.setEnabled(false);
        this.bEdytuj.setEnabled(true);
        this.bUsun.setEnabled(true);
        pracownicyComboBox.setEnabled(true);
        dane = bd.odczyt();
        pracownicyComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(initializeComboBox(dane)));
        pracownicyComboBox.setSelectedItem(tNrPracownika.getText());
    }//GEN-LAST:event_bZapiszActionPerformed

